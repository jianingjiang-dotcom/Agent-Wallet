export type Translations = typeof zh_CN;

export { translations };

/**
 * Hook that returns the translations object for the current language.
 *
 * Usage:
 * ```ts
 * const t = useT();
 * <span>{t.common.approve}</span>
 * ```
 */
export function useT(): Translations {
  const { language } = useLanguage();
  return translations[language];
}
